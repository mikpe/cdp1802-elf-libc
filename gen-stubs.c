static void gen_cbrt_stub(void)
{
    int i;
    union f64 arg;
    union f64 res;

    printf("static const struct stub_dd cbrt_stub = {\n");
    printf("    \"cbrt\", 0xA5, %zu, {\n", ARRAY_SIZE(cbrt_tab));
    for (i = 0; i < ARRAY_SIZE(cbrt_tab); ++i) {
	arg.ui = cbrt_tab[i];
	res.f = cbrt(arg.f);
	printf("\t{ {0x%016lx}, {0x%016lx} },\n", arg.ui, res.ui);
    }
    printf("    }\n};\n");
}

